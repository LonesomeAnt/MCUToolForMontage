unsigned char Xor (const unsigned char *pdata, unsigned short len)
{
    unsigned char val;
    unsigned short i;
        
    val = 0x00;
        
    for (i = 0; i < len; i++)
    {
        val ^= pdata[i]; 
    }
     
    return val;
}

